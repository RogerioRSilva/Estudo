#Com a função lambda vc não a nomei porem precisa atribuir a uma variável
    #Sendo assim quando colocamos o atributo 'X' ex: lambda X:
    # Em seguida só precisamos colcoar a ação a ser tomada usando esse atributo X
    # Ex: lambda X: X + 10
     
